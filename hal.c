#include	"hal.h"

/*------------------------------------------------------------------------------
//	Put byte in port
//----------------------------------------------------------------------------*/
void outb(u16int port, u8int value)
{

  asm volatile ("outb %1, %0"::"dN"(port),"a"(value));

}

/*------------------------------------------------------------------------------
//	Get byte from port
//----------------------------------------------------------------------------*/
u8int inb(u16int port)
{

  u8int ret;

  asm volatile ("inb %1, %0":"=a"(ret):"dN"(port));

  return ret;
}

/*------------------------------------------------------------------------------
//	Get byte from port
//----------------------------------------------------------------------------*/
u16int inw(u16int port)
{

  u16int ret;

  asm volatile ("inw %1, %0":"=a"(ret):"dN"(port));

  return ret;
}
