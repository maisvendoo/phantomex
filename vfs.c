#include	"vfs.h"

/*-----------------------------------------------------------------------------
 *
 *---------------------------------------------------------------------------*/
u32int	read(vfs_node_t* node, u32int offset, u32int size, u8int* buffer)
{
	if (node->read_vfs != 0)
		return node->read_vfs(node, offset, size, buffer);

	return 0;
}

/*-----------------------------------------------------------------------------
 *
 *---------------------------------------------------------------------------*/
u32int	write(vfs_node_t* node, u32int offset, u32int size, u8int* buffer)
{
	if (node->write_vfs != 0)
		return node->write_vfs(node, offset, size, buffer);

	return 0;
}

/*-----------------------------------------------------------------------------
 *
 *---------------------------------------------------------------------------*/
u32int open(vfs_node_t* node, u8int read, u8int write)
{
	if (node->open_vfs != 0)
		return node->open_vfs(node, read, write);

	return 0;
}

/*-----------------------------------------------------------------------------
 *
 *---------------------------------------------------------------------------*/
u32int close(vfs_node_t* node)
{
	if (node->close_vfs != 0)
		return node->close_vfs(node);

	return 0;
}
